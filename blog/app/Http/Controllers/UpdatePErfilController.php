    {
        
        $user = \Blog\User::find($id);
        $user->fill([
            'name' => $request['name'],
            'lname' => $request['lname'],
            'fecha_nac' => $request['fecha_nac'],
            'direccion' => $request['direccion'],
            ]);
        $user->save();
        Session::flash('message','Perfil actualizado');
        return Redirect('/profile');
